/**
 * Writes frontend/config.js from SOVEREIGN_ALPHA_API_URL for Vercel deploys.
 * Example: SOVEREIGN_ALPHA_API_URL=https://sovereign-alpha-api.onrender.com
 */
const fs = require("fs");
const path = require("path");

const apiRoot = (process.env.SOVEREIGN_ALPHA_API_URL || "http://localhost:8000").replace(/\/$/, "");
const wsRoot = apiRoot.replace(/^https:/, "wss:").replace(/^http:/, "ws:");

const config = {
  API_BASE: `${apiRoot}/api`,
  WS_URL: `${wsRoot}/ws/telemetry`,
  HEALTH_URL: `${apiRoot}/health`,
};

const outPath = path.join(__dirname, "..", "frontend", "config.js");
const contents = `/** Auto-generated by scripts/write-frontend-config.js — do not edit on Vercel. */\nwindow.SA_CONFIG = ${JSON.stringify(config, null, 2)};\n`;

fs.writeFileSync(outPath, contents);
console.log(`Wrote ${outPath} → API ${config.API_BASE}`);
